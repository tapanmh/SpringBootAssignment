package com.dev.challenge.geo.response;
/**
 * @author tapan
 *
 */
public class Coordinates {
	/**
	 * The latitude
	 */
	private double lat;
	/**
	 * The longitude
	 */
	private double lng;

	
	
	/**
	 * @return the latitude element
	 */
	public double getLat() {
		return lat;
	}

	
	
	
	/**
	 * @param latitude sets the latitude element
	 */
	public void setLat(double latitude) {
		this.lat = latitude;
	}

	
	
	
	/**
	 * @return the longitude element
	 */
	public double getLng() {
		return lng;
	}

	
	
	/**
	 * @param longitude sets the longitude element
	 */
	public void setLng(double longitude) {
		this.lng = longitude;
	}
	
}
